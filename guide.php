    <h4>But</h4>
    <p style="margin: 0 10%; text-align:justify;font-size:1em;">&nbsp;&nbsp;&nbsp;&nbsp;Après avoir reçu deux cartes, le joueur tire des cartes pour s’approcher de la valeur 21 sans la dépasser. Le but du joueur est de battre le croupier en obtenant un total de points supérieur à celui-ci ou en voyant ce dernier dépasser 21. Le joueur joue contre le croupier, qui représente la banque.</p>

    <h4>Valeurs des cartes</h4>
    <ul style="margin: 0 10%; text-align:justify; list-style-type:none;">
        <li>Chaque carte numérotée de 2 à 10 a sa valeur nominale (égale au numéro sur la carte)</li>
        <li>Les valets, les dames et les rois (les figures), ont une valeur de 10 points</li>
        <li>L’As vaut 1 point ou 11 points, il s’adapte pour être avantageux pour le Croupier comme pour le Joueur</li>
    </ul>

    <em>source: https://www.guide-blackjack.com</em>

    <h4>Déroulement</h4>
    <ul style="margin: 0 10%; text-align:justify; list-style-type:none;">
        <li>Le Joueur commence par placer sa mise.</li>
        <li>Le Joueur reçoit 2 cartes visibles et le Croupier 1 carte visible et 1 carte cachée.</li>
        <li>&nbsp;&nbsp;/Si le joueur obtient un score de 21 avec 1 AS et une Tête, c'est BlackJack et il reçoit 2X sa mise. La partie prend fin./</li>
        <li>Le joueur a ensuite 3 choix:</li>
            <ul>
                <li>(+) Hit: Le joueur reçoit une carte et choisi à nouveau s'il ne BUST pas (>21).</li>
                <li>(_) Stand: Le joueur se couche et le Croupier tire des cartes tant qu'il n'atteint pas 17. S'il ne BUST pas (>21), et qu'il a atteint 17, les scores sont comparés.</li>
                <li>(x2) Double: Le joueur double sa mise et tire 1 unique carte supplémentaire, puis Stand.</li>
            </ul>   
        <li>Résultats:</li>
            <ul style="margin: 0 10%; text-align:justify; list-style-type:none;">
                <li>'WIN' (comparaison des scores)</li>
                <li>'Lose' (comparaison des scores)</li>
                <li>'PUSH' (comparaison des scores - égalité)</li>
                <li>'BUST' (votre score > 21)</li>
                <li>'BLACKJACK' ( = 21 avec 2 cartes)</li>
            </ul>


